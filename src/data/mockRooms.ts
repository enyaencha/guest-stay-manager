export const calculateStats = (rooms: Room[]): DashboardStats => {
  const occupied = rooms.filter(r => r.occupancyStatus === 'occupied').length;
  const vacant = rooms.filter(r => r.occupancyStatus === 'vacant').length;
  const checkout = rooms.filter(r => r.occupancyStatus === 'checkout').length;
  const cleaning = rooms.filter(r => r.cleaningStatus === 'in-progress' || r.cleaningStatus === 'dirty').length;
  const maintenance = rooms.filter(r => r.maintenanceStatus !== 'none').length;
  
  return {
    totalRooms: rooms.length,
    occupied,
    vacant,
    cleaning,
    maintenance,
    checkoutsToday: checkout,
    checkinsToday: 2,
    occupancyRate: Math.round((occupied / rooms.length) * 100),
  };
};

